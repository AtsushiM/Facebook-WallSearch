<p> <strong>個人情報の取り扱いについて</strong><br />
<br />
Facebookアプリ、「Search Posts - ウォール検索」はFacebookから提供される機能を元に、<br />
一部の処理を除いてほぼすべてをクライアント（お使いのPC）内で動作するJavaScriptという言語で開発しています。<br />
<br />
その為、Facebookが元から持っている情報（ウォールの情報やアップしている画像情報など）を検索に使用していますが、<br />
そのデータを外部のデータベースなど、Facebook外に保存するなどの行為は<strong>一切していません。</strong><br />
※「★」機能ですが、これもブラウザの保存機能「localstorage」に対してデータを保存しているため、<br />
お使いのブラウザ以外には使用することができません。<br />
※この仕様は急遽変更になる可能性がありますが、可能な限りそれはしたくありません。</p>
